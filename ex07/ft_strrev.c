char	*ft_strrev(char *str)
{
	int		i1;
	int		i2;
	char 	temp;

	i1 = 0;
	i2 = 0;
	while (str[i1])
		i1++;
	i1--;
	while (i1 > i2)
	{
		temp = str[i1];
		str[i1] = str[i2];
		str[i2] = temp;
		i2++;
		i1--;
	}
	return (str);
}
